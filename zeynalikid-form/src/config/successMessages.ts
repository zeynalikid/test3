export const getRandomMessage = (messages: string[]) =>
  messages[Math.floor(Math.random() * messages.length)];

// انتخاب رندوم غیرتکراری در جلسه فعلی (امضای پیشرفته)
export const getRandomMessageTracked = (
  messages: string[],
  usedIndices: number[] = [],
): { message: string; newUsedIndices: number[] } => {
  if (!messages.length) return { message: '', newUsedIndices: usedIndices };
  let availableIndices = messages.map((_, i) => i).filter((i) => !usedIndices.includes(i));
  if (availableIndices.length === 0) {
    availableIndices = messages.map((_, i) => i);
    usedIndices = [];
  }
  const randomIndex = availableIndices[Math.floor(Math.random() * availableIndices.length)];
  return { message: messages[randomIndex], newUsedIndices: [...usedIndices, randomIndex] };
};
